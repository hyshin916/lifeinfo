package kr.co.mseshop.model;

public class FranchSellerVO {

	private String name;
	private String sellerCd;
	private String no;
	private String useCnt;
	private String passwd;
	private String comment;
	private String event;
	
	
	
	

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSellerCd() {
		return sellerCd;
	}

	public void setSellerCd(String sellerCd) {
		this.sellerCd = sellerCd;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getUseCnt() {
		return useCnt;
	}

	public void setUseCnt(String useCnt) {
		this.useCnt = useCnt;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

}
