package kr.co.mseshop.model;

public class MenuVO {

	private String menu_id;
	private String group_id;
	private String menu_parent;
	private String menu_name;
	private String menu_link;
	private String menu_target;
	private String menu_mobile;
	private String menu_pc;
	private String menu_order;

	public String getMenu_id() {
		return menu_id;
	}

	public void setMenu_id(String menu_id) {
		this.menu_id = menu_id;
	}

	public String getGroup_id() {
		return group_id;
	}

	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}

	public String getMenu_parent() {
		return menu_parent;
	}

	public void setMenu_parent(String menu_parent) {
		this.menu_parent = menu_parent;
	}

	public String getMenu_name() {
		return menu_name;
	}

	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}

	public String getMenu_link() {
		return menu_link;
	}

	public void setMenu_link(String menu_link) {
		this.menu_link = menu_link;
	}

	public String getMenu_target() {
		return menu_target;
	}

	public void setMenu_target(String menu_target) {
		this.menu_target = menu_target;
	}

	public String getMenu_mobile() {
		return menu_mobile;
	}

	public void setMenu_mobile(String menu_mobile) {
		this.menu_mobile = menu_mobile;
	}

	public String getMenu_pc() {
		return menu_pc;
	}

	public void setMenu_pc(String menu_pc) {
		this.menu_pc = menu_pc;
	}

	public String getMenu_order() {
		return menu_order;
	}

	public void setMenu_order(String menu_order) {
		this.menu_order = menu_order;
	}

}
